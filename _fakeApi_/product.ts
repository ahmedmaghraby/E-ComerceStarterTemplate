class ProductApi {
  getproductsAll(): Promise<productResponse[]> {
    return Promise.resolve(products);
  }

  getProductById(id: string): Promise<productResponse> {
    const product = products.find((product) => product.id === id);
    if (!product) {
      return Promise.reject(new Error("Product not found"));
    }
    return Promise.resolve(product);
  }

  getProductsByCategoryName(categoryName: string): Promise<productResponse[]> {
    const productsByCategory = products.filter(
      (product) => product.category && product.category.name === categoryName
    );
    return Promise.resolve(productsByCategory);
  }

  getProductsCountByCategoryName(categoryName: string): Promise<number> {
    const productsCount = products.filter(
      (product) => product.category && product.category.name === categoryName
    ).length;
    return Promise.resolve(productsCount);
  }

  searchProductBytName(productName: string): Promise<productResponse[]> {
    const productsSearch = products.filter((product) =>
      product.name.toLowerCase().includes(productName.toLowerCase())
    );
    return Promise.resolve(productsSearch);
  }
  /**
   *
   *
   * @param {number} [offSet=0]
   * @param {number} [limit=10]
   * @param {string} [categoryName=""]
   * @param {string} [orderBy="createdAt.dec"]
   * @return {*}  {Promise<productResponse[]>}
   * @memberof ProductApi
   */
  getProductsFilter(
    offSet: number = 0,
    limit: number = 10,
    categoryName: string = "",
    orderBy: string = "createdAt.dec"
  ): Promise<productResponse[]> {
    let productsFillter = products;
    if (categoryName.length >2 ) {
      productsFillter = productsFillter.filter(
        (product) => product.category && product.category.name === categoryName
      );
    }
    const  productsSort = productsFillter.sort((a, b) => {
      if (orderBy === "createdAt.dec" && b.createdAt && a.createdAt) {
        return (
          new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime()
        );
      }
      if (orderBy === "createdAt.asc" && b.createdAt && a.createdAt) {
        return (
          new Date(a.createdAt).getTime() - new Date(b.createdAt).getTime()
        );
      }
      if (orderBy === "price.dec") {
        return b.price - a.price;
      }
      if (orderBy === "price") {
        return a.price - b.price;
      }
      return 0;
    });
    const productsSlice = productsFillter.slice(offSet, offSet + limit);
    return Promise.resolve(productsSlice);
  }
}

